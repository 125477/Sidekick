/**
 * 扫描 `src/static/gif/*.gif`，重写 UI 里的 generated 列表。
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const gifDir = path.join(__dirname, '../src/static/gif')
const outFile = path.join(
  __dirname,
  '../src/assets/generated/gifBuiltinPresets.generated.ts',
)

function main() {
  if (!fs.existsSync(gifDir)) {
    console.warn('[sync-gif-builtin-presets] missing dir:', gifDir)
    fs.mkdirSync(gifDir, { recursive: true })
  }
  const names = fs.existsSync(gifDir)
    ? fs.readdirSync(gifDir, { withFileTypes: true })
    : []
  const files = names
    .filter((d) => d.isFile() && /\.gif$/i.test(d.name))
    .map((d) => d.name)
    .sort((a, b) => a.localeCompare(b, undefined, { sensitivity: 'base' }))

  const lines = files.map((f) => `  ${JSON.stringify(f)},`)
  const body = lines.length ? `\n${lines.join('\n')}\n` : ''

  const ts = `/* eslint-disable */
/**
 * Auto-generated by packages/ui/scripts/sync-gif-builtin-presets.mjs — do not edit.
 * 源目录: packages/ui/src/static/gif/
 */
import { buildGifBuiltinPresetsFromFilenames } from '../gifFolderBuiltinPresets'

const GIF_BUILTIN_FILES = [${body}] as const

export const GIF_FOLDER_BUILTIN_PRESETS =
  buildGifBuiltinPresetsFromFilenames(GIF_BUILTIN_FILES)
`

  fs.mkdirSync(path.dirname(outFile), { recursive: true })
  fs.writeFileSync(outFile, ts, 'utf8')
  console.log(
    '[sync-gif-builtin-presets] wrote',
    outFile,
    `(${files.length} files)`,
  )
}

main()
