/**
 * 扫描 `src/static/video/*.{mp4,webm,ogg,mov,m4v}`，重写 UI 里的 generated 列表。
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const videoDir = path.join(__dirname, '../src/static/video')
const outFile = path.join(
  __dirname,
  '../src/assets/generated/videoBuiltinPresets.generated.ts',
)

const VIDEO_EXT = /\.(mp4|webm|ogg|mov|m4v)$/i

function main() {
  if (!fs.existsSync(videoDir)) {
    console.warn('[sync-video-builtin-presets] missing dir:', videoDir)
    fs.mkdirSync(videoDir, { recursive: true })
  }
  const names = fs.existsSync(videoDir)
    ? fs.readdirSync(videoDir, { withFileTypes: true })
    : []
  const files = names
    .filter((d) => d.isFile() && VIDEO_EXT.test(d.name))
    .map((d) => d.name)
    .sort((a, b) => a.localeCompare(b, undefined, { sensitivity: 'base' }))

  const lines = files.map((f) => `  ${JSON.stringify(f)},`)
  const body = lines.length ? `\n${lines.join('\n')}\n` : ''

  const ts = `/* eslint-disable */
/**
 * Auto-generated by packages/ui/scripts/sync-video-builtin-presets.mjs — do not edit.
 * 源目录: packages/ui/src/static/video/
 */
import { buildVideoBuiltinPresetsFromFilenames } from '../videoFolderBuiltinPresets'

const VIDEO_BUILTIN_FILES = [${body}] as const

export const VIDEO_FOLDER_BUILTIN_PRESETS =
  buildVideoBuiltinPresetsFromFilenames(VIDEO_BUILTIN_FILES)
`

  fs.mkdirSync(path.dirname(outFile), { recursive: true })
  fs.writeFileSync(outFile, ts, 'utf8')
  console.log(
    '[sync-video-builtin-presets] wrote',
    outFile,
    `(${files.length} files)`,
  )
}

main()
