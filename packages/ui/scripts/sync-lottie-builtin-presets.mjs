/**
 * 扫描 `src/static/lotties/*.json`，重写 UI 里的 generated 列表，
 * 供 `buildLottieBuiltinPresetsFromFilenames` 使用（无需在 localStore 里逐个手写）。
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const lottiesDir = path.join(__dirname, '../src/static/lotties')
const outFile = path.join(
  __dirname,
  '../src/assets/generated/lottieBuiltinPresets.generated.ts',
)

function main() {
  if (!fs.existsSync(lottiesDir)) {
    console.warn('[sync-lottie-builtin-presets] missing dir:', lottiesDir)
    fs.mkdirSync(lottiesDir, { recursive: true })
  }
  const names = fs.existsSync(lottiesDir)
    ? fs.readdirSync(lottiesDir, { withFileTypes: true })
    : []
  const jsonFiles = names
    .filter((d) => d.isFile() && /\.json$/i.test(d.name))
    .map((d) => d.name)
    .filter((n) => {
      const lower = n.toLowerCase()
      if (lower === 'manifest.json') return false
      if (lower.endsWith('.generated.json')) return false
      return true
    })
    .sort((a, b) => a.localeCompare(b, undefined, { sensitivity: 'base' }))

  const lines = jsonFiles.map((f) => `  ${JSON.stringify(f)},`)
  const body = lines.length ? `\n${lines.join('\n')}\n` : ''

  const ts = `/* eslint-disable */
/**
 * Auto-generated by packages/ui/scripts/sync-lottie-builtin-presets.mjs — do not edit.
 * 源目录: packages/ui/src/static/lotties/
 */
import { buildLottieBuiltinPresetsFromFilenames } from '../lottieFolderBuiltinPresets'

const LOTTIE_BUILTIN_JSON_FILES = [${body}] as const

export const LOTTIE_FOLDER_BUILTIN_PRESETS =
  buildLottieBuiltinPresetsFromFilenames(LOTTIE_BUILTIN_JSON_FILES)
`

  fs.mkdirSync(path.dirname(outFile), { recursive: true })
  fs.writeFileSync(outFile, ts, 'utf8')
  console.log(
    '[sync-lottie-builtin-presets] wrote',
    outFile,
    `(${jsonFiles.length} files)`,
  )
}

main()
